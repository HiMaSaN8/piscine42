#include<unistd.h>
#include<stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb < 2147395600 && nb > 1)
	{
		while (i * i <= nb && i <= 46341)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
int main()
{
	printf("%d",ft_sqrt(-9));
}
