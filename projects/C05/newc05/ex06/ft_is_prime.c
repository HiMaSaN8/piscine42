#include<unistd.h>

int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	while (nb % i != 0 && i >= 2 && i <= 9)
	{
		i++;
		if (i == 9)
			return (1);
	}
	return (0);
}
int main()
{
	printf("%d",ft_is_prime(300));
}
