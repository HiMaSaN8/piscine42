#include<unistd.h>

int	main(int c, char **v)
{
	int	i;
	int	j;

	i = 0;
	j = c - 1;
	if (c > 1)
	{
		while (j > 0)
		{
			while (v[j][i] != 0)
			{
				write(1, &v[j][i], 1);
				i++;
			}
			write(1, "\n", 1);
			j--;
			i = 0;
		}
	}
}
